let firstNumber = Number(prompt("Перше число"));
let secondNumber = Number(prompt("Друге число"));

let sum = firstNumber + secondNumber;
let subtraction = firstNumber - secondNumber;
let multiplication = firstNumber * secondNumber;
let division = firstNumber / secondNumber;

alert(firstNumber + " + " + secondNumber + " = " + sum + "\n" +
    firstNumber + " - " + secondNumber + " = " + subtraction + "\n" +
    firstNumber + " * " + secondNumber + " = " + multiplication + "\n" +
    firstNumber + " / " + secondNumber + " = " + division);
